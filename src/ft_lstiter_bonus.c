#include "linked_lists.h"

void	ft_lstiter(LL_TYP *lst, void (*f)(LL_TYP *))
{
	if (!f)
		return ;
	if (lst)
	{
		f(lst);
		ft_lstiter(lst->next, f);
	}
}
