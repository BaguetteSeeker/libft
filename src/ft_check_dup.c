int	ft_check_dup(char *str)
{
	int	i;
	int	j;

	i = 0;
	j = 1;
	while (str[i] != '\0')
	{
		j = i + 1;
		while (str[j] != '\0')
		{
			if (str[i] == str[j])
				return (str[i]);
			j++;
		}
		i++;
	}
	return (0);
}
